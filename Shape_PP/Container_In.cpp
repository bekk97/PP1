#include <fstream>
using namespace std;

#include "Container.h"
#include "Shape.h"

//-------------------------------------------------------------------------------------------------
// Ввод содержимого контейнера из указанного потока
//-------------------------------------------------------------------------------------------------
void In(Container &c, ifstream &in)
{
	while (!in.eof())
		if ((c.cont[c.len] = In(in)) != 0)
			c.len++;
}
